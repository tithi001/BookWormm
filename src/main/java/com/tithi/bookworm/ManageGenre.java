    private void setList(){
        try {
            db.rs = db.stmt.executeQuery("select * from genre");
            DefaultListModel dlm = new DefaultListModel();
            while(db.rs.next()){
                dlm.addElement(db.rs.getString("genre_name"));
            }
            jList1.setModel(dlm);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
