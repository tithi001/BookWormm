    private void newGenreKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_newGenreKeyPressed
        if (evt.getKeyCode()==java.awt.event.KeyEvent.VK_ENTER){
            try {
                String genre = "'"+newGenre.getText()+"'";
                db.stmt.executeUpdate("insert into genre(genre_name) values("+genre+")");
                setList();
            } catch (SQLException ex) {
                ex.printStackTrace();
                String error_mssg = "ORA-00001: unique constraint (IPM.SYS_C0010637) violated";
                if(ex.getMessage().equals(error_mssg)){
                    JOptionPane.showMessageDialog(null, "This genre already exists.");
                }
            }
        jDialog1.setVisible(false);
        }
        if(evt.getKeyCode()==java.awt.event.KeyEvent.VK_ESCAPE){
            jDialog1.setVisible(false);
        }
    }//GEN-LAST:event_newGenreKeyPressed

